DROP TABLE IF EXISTS evolutions;

CREATE TABLE evolutions (id INT NOT NULL AUTO_INCREMENT,
            from_id INT,
            to_id INT,
            candy_cost INT,
            PRIMARY KEY ( id ),
            FOREIGN KEY (from_id) REFERENCES pokemon(id),
            FOREIGN KEY (to_id) REFERENCES pokemon(id)
          );

